#ifndef H2BACKGROUND_H
#define H2BACKGROUND_H

#include "rect.h"
#include "gamedefs.h"

class Background
{

public:
    Background(const Rect &rback) : surface(NULL), rect(rback){ Save(); };
    ~Background(){ delete surface; };

    void Save(const Rect &rback){ rect = rback; Save(); };
    void Save(void);
    void Restore(void);

private:
    SDLmm::Surface *surface;
    Rect rect;
};

#endif
